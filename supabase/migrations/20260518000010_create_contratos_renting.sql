  'Coluna GENERATED ALWAYS — NÃO escrever manualmente. Base do EXCLUDE anti-overbooking.';


-- ============================================================
-- Anti-overbooking: viatura não pode estar em 2 contratos activos sobrepostos
-- Activo = estado_operacional IN ('agendado', 'em_curso')
-- ============================================================
DO $$ BEGIN
  ALTER TABLE public.contratos_renting
    ADD CONSTRAINT contratos_no_overbooking
    EXCLUDE USING gist (
      org_id     WITH =,
      viatura_id WITH =,
      periodo    WITH &&
    ) WHERE (
      deleted_at IS NULL
      AND estado_operacional IN ('agendado', 'em_curso')
    );
EXCEPTION
  WHEN duplicate_object THEN NULL;
END $$;


-- ============================================================
-- Trigger cross-table: contrato não pode ser criado se houver
-- reserva activa sobreposta para a mesma viatura (excepto se reserva_id
-- aponta exactamente para essa reserva — caso reserva→contrato).
-- ============================================================
CREATE OR REPLACE FUNCTION public.fn_contratos_validar_reserva()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  IF NEW.viatura_id IS NULL OR NEW.deleted_at IS NOT NULL THEN
    RETURN NEW;
  END IF;

  -- Estados activos do contrato bloqueiam
  IF NEW.estado_operacional NOT IN ('agendado', 'em_curso') THEN
    RETURN NEW;
  END IF;

  IF EXISTS (
    SELECT 1 FROM public.reservas r
    WHERE r.org_id = NEW.org_id
      AND r.viatura_id = NEW.viatura_id
      AND r.deleted_at IS NULL
      AND r.estado IN ('pendente', 'confirmada', 'em_curso')
      AND r.periodo && NEW.periodo
      -- Excepção: reserva que ORIGINOU este contrato não conflita consigo mesma
      AND (NEW.reserva_id IS NULL OR r.id <> NEW.reserva_id)
  ) THEN
    RAISE EXCEPTION
      'Conflito: viatura já tem reserva activa sobreposta para este período.'
      USING ERRCODE = 'exclusion_violation';
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_contratos_renting_validar_reserva ON public.contratos_renting;
CREATE TRIGGER trg_contratos_renting_validar_reserva
BEFORE INSERT OR UPDATE OF viatura_id, data_inicio, data_fim, estado_operacional, deleted_at
ON public.contratos_renting
FOR EACH ROW EXECUTE FUNCTION public.fn_contratos_validar_reserva();


