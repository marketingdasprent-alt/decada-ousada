-- CONTRATOS
-- ============================================================
CREATE TABLE IF NOT EXISTS public.contratos_renting (
  id                          UUID PRIMARY KEY DEFAULT gen_random_uuid(),

  org_id                      UUID NOT NULL REFERENCES public.organizacoes(id) ON DELETE CASCADE
                              DEFAULT public.get_current_org_id(),

  codigo                      BIGINT GENERATED BY DEFAULT AS IDENTITY,

  -- Ligação opcional a reserva. UNIQUE — uma reserva = no máximo 1 contrato.
  reserva_id                  UUID UNIQUE REFERENCES public.reservas(id) ON DELETE SET NULL,

  -- Cliente (obrigatório, sem snapshot — FK RESTRICT garante validade)
  cliente_id                  UUID NOT NULL REFERENCES public.clientes(id) ON DELETE RESTRICT,

  -- Viatura (obrigatória; snapshot matrícula porque viatura pode mudar matrícula com o tempo)
  viatura_id                  UUID NOT NULL REFERENCES public.viaturas(id) ON DELETE RESTRICT,
  matricula                   VARCHAR(20),   -- snapshot histórico
  grupo                       VARCHAR(50),   -- texto livre (categoria visual)

  -- Entrega
  estacao_entrega_id          UUID REFERENCES public.estacoes(id) ON DELETE SET NULL,
  data_inicio                 TIMESTAMPTZ NOT NULL,

  -- Recolha
  estacao_recolha_id          UUID REFERENCES public.estacoes(id) ON DELETE SET NULL,
  data_fim                    TIMESTAMPTZ NOT NULL,

  -- Estação ORIGEM da viatura (de onde sai fisicamente)
  estacao_origem_viatura_id   UUID REFERENCES public.estacoes(id) ON DELETE SET NULL,

  -- Estados (2 dimensões)
  estado_operacional          public.contrato_estado_operacional_enum NOT NULL DEFAULT 'agendado',
  estado_financeiro           public.contrato_estado_financeiro_enum NOT NULL DEFAULT 'pendente',

  origem                      public.contrato_origem_enum NOT NULL DEFAULT 'sistema',

  -- Tarifário (MVP — manual; v2 ligará a tabela `tarifas`)
  tarifa_diaria               NUMERIC(10, 2),
  desconto_percentagem        NUMERIC(5, 2) DEFAULT 0,
  taxa_iva                    NUMERIC(5, 2) NOT NULL DEFAULT 23,
  valor_total_manual          NUMERIC(10, 2),  -- override directo do total (bypass cálculo)

  -- Snapshot de totais — congelados quando estado_financeiro = 'facturado'
  -- NULL enquanto pendente. Imutáveis após facturação (compliance SAF-T).
  total_subtotal              NUMERIC(10, 2),
  total_iva                   NUMERIC(10, 2),
  total_final                 NUMERIC(10, 2),
  facturado_em                TIMESTAMPTZ,

  -- Voucher
  voucher_codigo              VARCHAR(50),

  -- Informação adicional
  numero_processo             VARCHAR(100),
  voo_referencia              VARCHAR(100),     -- ex.: "TP1234 chegada / TP5678 partida"
  local_entrega               VARCHAR(255),
  local_recolha               VARCHAR(255),
  comentarios_entrega         TEXT,
  comentarios_recolha         TEXT,

  observacoes                 TEXT,

  -- Soft delete
  deleted_at                  TIMESTAMPTZ,

  -- Auditoria
  created_by                  UUID REFERENCES auth.users(id) ON DELETE SET NULL DEFAULT auth.uid(),
  updated_by                  UUID REFERENCES auth.users(id) ON DELETE SET NULL,
  created_at                  TIMESTAMPTZ NOT NULL DEFAULT timezone('utc', now()),
  updated_at                  TIMESTAMPTZ NOT NULL DEFAULT timezone('utc', now()),

  -- Coluna gerada (base do EXCLUDE)
  periodo                     TSTZRANGE GENERATED ALWAYS AS (
                                tstzrange(data_inicio, data_fim, '[)')
                              ) STORED,

  CONSTRAINT chk_contratos_periodo_valido CHECK (data_fim > data_inicio),
  CONSTRAINT chk_contratos_iva_valido CHECK (taxa_iva >= 0 AND taxa_iva <= 100),
  CONSTRAINT chk_contratos_desconto_valido CHECK (
    desconto_percentagem IS NULL OR (desconto_percentagem >= 0 AND desconto_percentagem <= 100)
  ),
  CONSTRAINT chk_contratos_totais_facturados CHECK (
    -- Se facturado, totais e data de facturação devem estar preenchidos
    estado_financeiro <> 'facturado'
    OR (total_final IS NOT NULL AND facturado_em IS NOT NULL)
  ),
  CONSTRAINT contratos_renting_codigo_org_unique UNIQUE (org_id, codigo)
);

