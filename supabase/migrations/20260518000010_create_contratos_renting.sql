COMMENT ON COLUMN public.contratos_renting.estado_financeiro IS
  'Ciclo de facturação: pendente → facturado → pago. anulado retorna a fluxo.';

COMMENT ON COLUMN public.contratos_renting.total_final IS
  'Snapshot do total facturado. NULL enquanto estado_financeiro=pendente. '
  'Congelado quando muda para facturado (compliance SAF-T — não altera retroactivamente).';

